            <div class="mt20 mb10">
              菏泽市人民政府关于积极应对新冠肺炎疫情做好稳就业工作的通知

 

菏政发〔2020〕3号

各县区人民政府，市开发区、高新区管委会，市政府各部门、各直属机构，市直各单位，市属各企业，各大中专院校：

根据《山东省人民政府印发关于积极应对新冠肺炎疫情做好稳就业工作的若干措施的通知》（鲁政发〔2020〕5号）文件精神，结合我市实际，为做好稳就业工作，现将有关事项通知如下：

一、设立用工专员，落实用工补贴

市县两级工信、发改部门负责落实重点企业和重点项目名单，由企业或项目所在地人社部门设立用工服务专员，提供“一对一”用工服务，全力保障企业用工需求。市县两级工信部门负责落实疫情防控重点物资生产企业名单等相关信息，企业所在地人社部门会同财政部门抓好一次性用工补贴和一次性吸纳就业补贴的落实和发放。

二、开展线上招聘，助力返岗复工

市县两级人社部门以政府部门、企事业单位等用工门户网站为载体，长期开展线上招聘活动，为缺工企业精准匹配闲置人员，形成覆盖全市的招聘网络，为企业复工用工保驾护航。交通运输部门要针对企业需求，组织点对点定制化运输服务。卫生健康部门指导企业做好卫生防疫、体温检测等工作，严格落实防疫措施要求。

三、落实减免政策，扶持重点群体

市县两级财政、税务、人社、医保、残联、住房公积金等部门要严格按照国务院、省政府最新要求，落实好社保费、医保费减免、稳岗返还、残保金减缓、住房公积金减缓等政策，为企业减税降费，帮助企业渡过难关。市县两级人社部门会同财政等相关部门大力实施青年见习计划，落实最新的见习政策；充分运用社补、岗补等政策吸纳困难人员就业；抓好重点行业职工、退役军人、残疾人、妇女等特殊群体就业扶持。

四、加强技能培训，推进职业教育

市县两级职业技能提升行动有关责任单位要聚焦我市主导产业，全面开展“互联网+职业培训”，并着手疫情后培训实操的组织、职业鉴定的开展、培训补贴的发放等各项工作，年内培训各类人员8万人以上。市县两级人社部门要切实抓好企业金蓝领培训，推进企业新型学徒制、企业技能人才自主评价制度；市教育部门牵头负责至少建设1处共享性的大型智能（仿真）实习实训基地，提升人才技能水平。   

五、深化创业引领，鼓励返乡创业

市县两级人社部门会同财政等部门全面落实创业孵化基地（园区）运营补贴、创业担保贷款等政策；科技部门负责有关众创空间、科技企业孵化器疫情防控期间出台政策的落实。市县两级人社部门负责牵头落实市外返乡创业服务站建设，并根据市县两级返乡创业服务站服务效果给予每个服务站3万元资金奖补，所需资金从创业带动就业扶持资金、就业补助资金中列支；相关部门做好各类乡创平台建设，返乡创业项目批审、服务成果奖补等政策的落实。

六、保障工资支付，稳定劳动关系

市县两级人社、住建、交通、水利等部门要加大农民工工资支付监管平台推广应用力度，下大气力抓好农民工实名制管理、工资专户、银行代发工资等制度的落实，实现农民工工资支付网上动态监管，确保农民工工资按时足额发放。充分发挥协调劳动关系三方作用，规范企业裁员行为，防止出现大规模裁员。对拟进行经济性裁员的企业，指导其依法依规制定和实施职工安置方案。

七、加强组织领导，防范失业风险

市县两级就业工作领导小组成员单位要提高政治站位、加强组织领导，实事求是做好本领域监测数据上报，加强部门间协作，加强数据比对，做好失业风险预警。编制部门统筹市县编制资源，落实公共就业和人才服务机构编制标准。财政部门会同人社部门通过财政预算安排或就业补助资金设立就业风险储备金，用于应对突发性、规模性失业风险，落实临时性、短期性促就业、防失业、保稳定政策。

八、加大资金支持，强化政策宣传

自2020年起，市级创业带动就业扶持资金由不低于1500万元提高至3500万元，所需资金从失业保险基金结余中安排。市县两级宣传部门牵头做好稳就业各项政策的宣传报道，提高政策知晓度。各级各部门要进一步改进工作作风，推动职能由“管理者”向“服务者”转变，政策落实由“人找政策”向“政策找人”转变，业务办理由“见面审批”向“线上办理”转变。 

未尽事宜，以鲁政发〔2020〕5号文件为准。

 

菏泽市人民政府

2020年2月26日

